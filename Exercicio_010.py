# Faça um programa que peça a temperatura em graus Celsius, transforme e mostre em graus Fahrenheit.

g_c = float(input("Qual a temperatura em Celsius?")) # entrada de variavel
txt = "A temperatura convertida em Fahrenheint é: " # Textinho
g_f = (g_c*9/5)+32 # calculo da conversão
print(txt, g_f) # saída

