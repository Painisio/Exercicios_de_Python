# Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

ray = float(input("Defina o Raio do Círculo: ")) # Variável de Entrada
pi = 3.1415 # Valor assumido de π (pi)
area_of_circle = (ray**2)*pi # Calculo
texto = "A área do círculo para o raio informado é: " # Texto pra fazer o charme, e não ficar apenas a matemática =)
print(texto, area_of_circle) # Sáida do Código

