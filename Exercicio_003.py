# Faça um Programa que peça dois números e imprima a soma.

n1 = int(input("Insira o primeiro número: ")) # Primeira variável
n2 = int(input("Insira o segundo número: ")) # Segunda variável
soma = n1 + n2 # Soma o primeiro com o segundo número
texto = "A soma dos dois números é: " # Texto a ser exibido!

print(texto, soma)

